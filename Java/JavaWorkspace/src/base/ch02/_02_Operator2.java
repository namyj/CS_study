package base.ch02;

// (복합) 대입 연산자
public class _02_Operator2 {
    public static void main(String[] args) {
        int num = 10;
        num += 2;
        System.out.println(num);
        
        num -= 2;
        System.out.println(num);

        num *= 2;
        System.out.println(num);

        num /= 2;
        System.out.println(num);

        num %= 2;
        System.out.println(num);
    }
}
