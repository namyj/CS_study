package backjoon.base2;

import java.util.Scanner;

public class Prob10869 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int aVal = sc.nextInt();
        int bVal = sc.nextInt();
        System.out.println(aVal + bVal);
        System.out.println(aVal - bVal);
        System.out.println(aVal * bVal);
        System.out.println(aVal / bVal);
        System.out.println(aVal % bVal);
    }
}
